class LoopTrack {
  const LoopTrack({
    required this.roleTitle,
    required this.company,
    required this.level,
    required this.cyclesCompleted,
    required this.delta,
    required this.progress,
    required this.focus,
  });

  final String roleTitle;
  final String company;
  final double level;
  final int cyclesCompleted;
  final double delta;
  final double progress;
  final String focus;
}
